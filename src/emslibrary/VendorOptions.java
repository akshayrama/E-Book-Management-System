    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        try {
            Class.forName("com.mysql.jdbc.Driver");
            Connection con=DriverManager.getConnection("jdbc:mysql://localhost:3306/emslibrary","root","password");
            //Check if the username exists
            String sql1 = "select usernamev from emslibrary.vendors";
            String givenUsername = jTextField1.getText();
            PreparedStatement stmt1 = con.prepareStatement(sql1);
            ResultSet rs1 = stmt1.executeQuery();
            int flag = 0;
            while(rs1.next()){
                if(rs1.getString("usernamev").equals(givenUsername)){
                    flag = 1;
                    break;
                }
            }
            if (flag == 1){
                String sql = "select passwordv from emslibrary.vendors where usernamev = ?";
                PreparedStatement stmt=con.prepareStatement(sql); 
                stmt.setString(1,jTextField1.getText());
                ResultSet rs=stmt.executeQuery(); 
                String checkPass = null;
                while(rs.next())  
                      checkPass = rs.getString("passwordv");
                if (checkPass.equals(jTextField2.getText())){
                    this.dispose();
                    VendorMenu vm = new VendorMenu(givenUsername);
                    vm.setVisible(true);
                }
                else {
                    JOptionPane.showMessageDialog(this, "Invalid Password!");
                    jTextField2.setText("");
                }
            }
            else {
                JOptionPane.showMessageDialog(this, "The username does not exist! Please try again or register");
                jTextField1.setText("");
                jTextField2.setText("");
            }           
            con.close();  
        }
        catch(Exception e){
            System.out.println(e);
        }
        
    }//GEN-LAST:event_jButton2ActionPerformed

